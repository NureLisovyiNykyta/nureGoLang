package main

import (
	"fmt"
)

func task2() {
	// Мапа для зберігання місяців: номер -> назва наступного місяця
	months := map[int]string{
		1:  "Лютий",
		2:  "Березень",
		3:  "Квітень",
		4:  "Травень",
		5:  "Червень",
		6:  "Липень",
		7:  "Серпень",
		8:  "Вересень",
		9:  "Жовтень",
		10: "Листопад",
		11: "Грудень",
		12: "Січень",
	}

	var month int
	fmt.Println("Введіть номер місяця (1-12):")
	fmt.Scan(&month)

	// Перевірка, чи є номер місяця в мапі
	if nextMonth, exists := months[month]; exists {
		fmt.Println(nextMonth)
	} else {
		fmt.Println("Некоректний номер місяця. Введіть число від 1 до 12.")
	}
}